package loopingStatements;

public class PositiveOrNegativeOrZero {
	
	public static void main(String[] args)
	{
		int n=3;
		
		if(n > 0)
		{
			System.out.println("Numer is positive");
		}
		else if(n<0)
		{
			System.out.println("Numer is negative");
		}
		else
		{
			System.out.println("Numer is zero");
		}
				
	}

}
